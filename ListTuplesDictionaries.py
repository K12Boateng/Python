# Creating a list containing three fruit strings
myFruitList = ["apple", "banana", "cherry"]
# Printing the list of fruits
print(myFruitList)

# Printing the data type of myFruitList
print(type(myFruitList))

# Accessing and printing the first element of the list
print(myFruitList[0])

# Accessing and printing the second element of the list
print(myFruitList[1])

# Accessing and printing the third element of the list
print(myFruitList[2])

# Modifying the third element of the list
myFruitList[2] = "orange"
# Printing the updated list
print(myFruitList)

